#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*char_s;
	size_t	i;

	char_s = s;
	i = 0;
	if (n > 0)
	{
		while (i < n)
		{
			char_s[i] = 0;
			i++;
		}
	}
}
